// 📌원시 타입 (Number, String, Boolean 등)
// -> 값 자체로써 변수에 저장되고 복사됨.
// 👉 불변값임 (메모리 값 수정X)

// 📌객체 타입(Object, Array, Function 등)
// -> 참조값을 통해 변수에 저장되고 복사됨.
// 👉 가변값임(메모리 값 수정O)

// ✨ 객체 간의 비교는 기본적으로 참조값을 기준으로 이루어짐!  
let o1 = { name: "조재윤" };
let o2 = o1;  // 객체의 참조값을 복사한 얕은 복사
let o3 = { ...o1 };   // spread연산자를 이용한 깊은 복사 : 새로운 객체를 생성하면서 프로퍼티만 따로 복사함.

console.log(o1 === o2);   // true
console.log(o1 === o3);   // false 참조값이 서로 다른 두 객체이므로!

